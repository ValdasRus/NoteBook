
public class ContactNote {
	
	private String title;
	
	private String note;
	
	public void setTitle(String titlename) {
		title = titlename;
	}
	
	public void setNote(String notename) {
		note = notename;
	}
	
	public String getTitle() {
		return title;
	}
	
	public String getNote() {
		return note;
	}


}
